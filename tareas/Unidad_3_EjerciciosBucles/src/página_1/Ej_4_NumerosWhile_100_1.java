package página_1;

public class Ej_4_NumerosWhile_100_1 {
	
	//1.- PROGRAMA QUE MUESTRE LOS NÚMEROS DEL 100 AL 1 UTILIZANDO WHILE

	public static void main(String[] args) {
		
		// DECLARACIÓN DE VARIABLES
		
		int num=100;
		
		// EJECUCIÓN

		while(num>=1) {
			System.out.println(num--);
		}
		
	}

}
