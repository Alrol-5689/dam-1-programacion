package página_1;

public class Ej_1_NumerosWhile_1_100 {
	
	//1.- PROGRAMA QUE MUESTRE LOS NÚMEROS DEL 1 AL 100 UTILIZANDO WHILE

	public static void main(String[] args) {
		
		// DECLARACIÓN DE VARIABLES
		
		int num=1;
		
		// EJECUCIÓN

		while(num<=100) {
			System.out.println(num++);
		}
		
	}

}
